op = int(input("Digite o número correspondente ao dia da semana\n1 - Domingo\n2 - Segunda\n3 - Terça\n4 - Quarta\n5 - Quinta\n6 - Sexta\n7 - Sábado\n:"))

if op == 1: 
    print("Domingo")

elif op == 2: 
    print("Segunda")

elif op == 3: 
    print("Terça")

elif op == 4: 
    print("Quarta")

elif op == 5: 
    print("Quinta")

elif op == 6: 
    print("Sexta")

elif op == 7: 
    print("Sábado")

else:
    print("Opção inválida!")
